  <h2>About this demo</h2>
  <p>This is a demo of the various tracking features of Enhanced Ecommerce in Google Analytics. It's implemented using Google Tag Manager.</p>
  <p>What's being tracked:
  <ul>
    <li>Product impressions (when this page is loaded, each product at the top of this page is tracked as an impression)</li>
    <li>Product clicks (when you click on a product at the top of this page)</li>
    <li>Product detail views (the product page with details about the product)</li>
    <li>Add to shopping cart</li>
    <li>Remove from shopping cart</li>
    <li>Checkout funnel steps</li>
    <li>Checkout options (coupon code and payment option)</li>
    <li>Promotion impressions (the banners on this page)</li>
    <li>Promotion clicks (when you click on a promotion)</li>
  </ul>
